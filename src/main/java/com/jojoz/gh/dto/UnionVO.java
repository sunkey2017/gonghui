package com.jojoz.gh.dto;

public class UnionVO {
	
	private String id ;
	private int state;
	private String unionCardCode;
	private String legal;
	private String unionName;
	private String produceDivision;
	private String user_id;
	
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public int getState() {
		return state;
	}
	public void setState(int state) {
		this.state = state;
	}
	public String getLegal() {
		return legal;
	}
	public void setLegal(String legal) {
		this.legal = legal;
	}
	public String getUnionName() {
		return unionName;
	}
	public void setUnionName(String unionName) {
		this.unionName = unionName;
	}
	public String getProduceDivision() {
		return produceDivision;
	}
	public void setProduceDivision(String produceDivision) {
		this.produceDivision = produceDivision;
	}
	public String getUnionCardCode() {
		return unionCardCode;
	}
	public void setUnionCardCode(String unionCardCode) {
		this.unionCardCode = unionCardCode;
	}
	public String getUser_id() {
		return user_id;
	}
	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}
	
	
	
	

}
